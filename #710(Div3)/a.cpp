#include <bits/stdc++.h>
using namespace std;

void solve() {
	long long h, w, n;
	scanf("%lld%lld%lld",&h, &w, &n);
	n--;
	long long x = n/h;
	long long y = n%h;
	long long ans = x+1 + y*w;
	printf("%lld\n", ans);
}

int main() {
	long long t=0;
	cin >> t;
	while(t--) {
		//cout << "Case #" << ++i << ": ";
		solve();
	}
	return 0;
}
